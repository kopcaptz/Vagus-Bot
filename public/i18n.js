let currentLang = localStorage.getItem('vagus_lang') || 'ru';

function t(key, ...args) {
  let str = (i18n[currentLang] && i18n[currentLang][key]) || i18n.ru[key] || key;
  args.forEach((a, i) => {
    str = str.replace(`{${i}}`, a);
  });
  return str;
}

function setLang(lang) {
  if (lang !== 'en' && lang !== 'ru') return;
  currentLang = lang;
  localStorage.setItem('vagus_lang', lang);
  if (typeof applyTranslations === 'function') {
    applyTranslations();
  }
}
